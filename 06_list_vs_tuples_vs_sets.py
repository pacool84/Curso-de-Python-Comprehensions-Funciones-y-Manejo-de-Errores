# Comparación de Listas, Tuplas y Conjuntos (Sets) en Python

# | Tipo  | Mutable | Ordenada | Indexing/Slicing | Duplicar elementos |
# |-------|---------|----------|------------------|---------------------|
# | List  |   ✔️    |    ✔️    |        ✔️       |         ✔️         |
# | Tuple |   ❌    |    ✔️    |        ✔️       |         ✔️         |
# | Set   |   ✔️    |    ❌    |        ❌       |         ❌         |

# Descripción:
# - Mutable: Define si los elementos pueden modificarse después de la creación.
# - Ordenada: Indica si los elementos se almacenan en un orden fijo.
# - Indexing/Slicing: Si es posible acceder a los elementos a través de índices o realizar cortes (slicing).
# - Duplicar elementos: Define si permite elementos duplicados.

# Ejemplos:
# - List: Puede modificar elementos, es ordenada, permite acceso por índice, y admite duplicados.
# - Tuple: No es mutable, es ordenada, permite acceso por índice, y admite duplicados.
# - Set: Es mutable, no es ordenada, no permite acceso por índice, y no admite duplicados.